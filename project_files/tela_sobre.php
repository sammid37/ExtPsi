    	<p style="text-indent:4em; font-size: 12pt; text-align: justify;" class="dark-violet-text">
    	Os alunos do Campus Caicó encontram-se com dificuldades em manter um relacionamento com a psicóloga local, vendo isso, o projeto tem como intuito melhorar essa comunicação entre os mesmos, trazendo de forma dinâmica e prática, uma extensão para que os alunos/pacientes possam manter contato com a psicóloga e agendar consultas presenciais.
    	A depressão nos alunos, é uma consequência de uma fase ruim da adolescência/juventude, misturados com problemas em casa, escola, relacionamentos entre outros. A falta de acesso à
    	uma conversa para determinar uma solução para os problemas enfrentados causa um impacto ainda maior na vida dos alunos. O campus Caicó agora disponibiliza acesso e consultas com a psicóloga local, onde o aluno pode se comunicar com ela. Mas não são todos que vão por vontade própria. Muitos alunos se encontram em situação ruim e não procuram ajuda por vergonha, medo ou outro motivo. Pensando nisso, construímos um site que estabelece uma conexão virtual onde o aluno pode relatar seus sentimentos, seu humor e marcar uma consulta. O meio em que ele usa é bem mais acessível e mais prático. Sem precisar de uma relação direta, indo apenas no dia marcado para sua conversa com a psicóloga. Evitando constrangimentos etc.</p>
